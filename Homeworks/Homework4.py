"""15.	Дан массив размера N и целые числа K и L (1 < K ≤ L ≤ N). 
Найти среднее арифметическое всех элементов массива, кроме элементов 
с номерами от K до L включительно."""

a = [1, 2, 3, 4, 5, 6, 7, 8, 9]
k = 5
l = 5

