"""1.	Дано целое число N (> 0). Сформировать и вывести целочисленный массив размера N, 
содержащий N первых положительных нечетных чисел: 1, 3, 5, … ."""""
import math

n = 3
n1 = [i for i in range(2 * n + 1) if i % 2 != 0]

print("Задание 1")
print(n1)

"""2.	Дано целое число N (> 0). Сформировать и вывести целочисленный массив размера N, 
содержащий степени двойки от первой до N-й: 2, 4, 8, 16, … ."""

n = 5
n1 = [2 ** i for i in range(1, n + 1)]

print("Задание 2")
print(n1)

"""3.	Дано целое число N (> 1), а также первый член A и разность D арифметической прогрессии. 
Сформировать и вывести массив размера N, содержащий N первых членов данной прогрессии: 
A, A + D, A + 2·D, A + 3·D, … ."""

n = 5
a = 1
d = 4
n1 = [a + i * d for i in range(n)]

print("Задание 3")
print(n1)

"""4.	Дано целое число N (> 1), а также первый член A и знаменатель D геометрической 
прогрессии. Сформировать и вывести массив размера N, содержащий N первых членов данной 
прогрессии: A, A·D, A·D2, A·D3, … ."""

n = 5
a = 1
d = 5
n1 = [a * d ** i for i in range(n)]

print("Задание 4")
print(n1)

"""5.	Дано целое число N (> 2). Сформировать и вывести целочисленный массив размера N, 
содержащий N первых элементов последовательности чисел Фибоначчи FK: F1 = 1, F2 = 1, 
FK = FK–2 + FK–1, K = 3, 4, … ."""

n = 5
n1 = [int((((1 + math.sqrt(5)) / 2) ** i - ((1 - math.sqrt(5)) / 2) ** i) / math.sqrt(5)) for i in range(1, n + 1)]

print("Задание 5")
print(n1)

"""6.	Даны целые числа N (> 2), A и B. Сформировать и вывести целочисленный массив 
размера N, первый элемент которого равен A, второй равен B, а каждый последующий элемент 
равен сумме всех предыдущих."""

n = 5
a = 2
b = 6
n1 = [2 ** i * (a + b) if i != -2 and i != -1 else a if i == -2 else b for i in range(-2, n - 2)]

print("Задание 6")
print(n1)

