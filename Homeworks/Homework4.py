"""16.	Дан целочисленный массив размера N, не содержащий одинаковых чисел. 
Проверить, образуют ли его элементы арифметическую прогрессию. Если образуют, 
то вывести разность прогрессии, если нет — вывести 0."""

a = [1, 2, 3, 4, 5, 6, 7, 8, 9]
d = [a[i + 1] - a[i] for i in range(len(a) - 1)]

print("Задание 16")
print(d[0] if len(set(d)) == 1 else 0)

"""17.	Дан массив ненулевых целых чисел размера N. Проверить, образуют ли его элементы 
геометрическую прогрессию. Если образуют, то вывести знаменатель прогрессии, если нет — вывести 0."""

a = [1, 2, 3, 4, 5, 6, 7, 8, 9]
d = [a[i + 1] / a[i] for i in range(len(a) - 1)]

print("Задание 17")
print(d[0] if len(set(d)) == 1 else 0)

"""18.	Дан целочисленный массив размера N. Проверить, чередуются ли в нем четные и нечетные числа. 
Если чередуются, то вывести 0, если нет, то вывести порядковый номер первого элемента, 
нарушающего закономерность."""

a = [1, 2, 3, 4, 5, 6, 7, 8, 9]

print("Задание 18")
print(next(iter(x for x in enumerate(zip(a[:-1], a[1:])) if x[1][0] % 2 == x[1][1] % 2), [-2])[0] + 2)
